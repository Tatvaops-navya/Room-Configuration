/**
 * Reads IKEA block export (tabular: category, name, price, description, images, url per record).
 * Default input: scripts/tmp_ikea_paste.txt — or pass path: node scripts/generate-mattress-seed-sql.mjs ./my-paste.txt
 * Output: supabase/migrations/012_mattress_products_seed.sql
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(__dirname, '..')
const inputPath = process.argv[2] ?? path.join(__dirname, 'tmp_ikea_paste.txt')

if (!fs.existsSync(inputPath)) {
  console.error('Missing input file:', inputPath)
  process.exit(1)
}

const raw = fs.readFileSync(inputPath, 'utf8')
const lines = raw.split(/\r?\n/)

function normalizeText(s) {
  return s
    .replaceAll('√Ö', 'Ö')
    .replaceAll('√§', 'ä')
    .replaceAll('‚Äì', '–')
    .replaceAll('It´s', "It's")
    .replaceAll('it´s', "it's")
    .trim()
}

/** Name must look like a mattress / pad / cot mattress / pouffe mattress product */
function isMattressProduct(name) {
  const n = name.toLowerCase()
  if (/\bmattress(es)?\b|mattress pad|pouffe\/mattress/i.test(n)) return true
  return false
}

function sqlStr(s) {
  return "'" + String(s).replaceAll("'", "''") + "'"
}

const blocks = []
let i = 0
while (i < lines.length) {
  const line = lines[i].trim()
  if (!line) {
    i++
    continue
  }
  // category: IKEA path
  if (!line.startsWith('in/en/cat/')) {
    i++
    continue
  }
  const category = line
  const name = (lines[i + 1] ?? '').trim()
  const priceLine = (lines[i + 2] ?? '').trim()
  const desc = (lines[i + 3] ?? '').trim()
  const imagesLine = (lines[i + 4] ?? '').trim()
  const urlLine = (lines[i + 5] ?? '').trim()
  i += 6

  if (!name || !/^\d+$/.test(priceLine) || !desc || !imagesLine.startsWith('http') || !urlLine.startsWith('http')) {
    continue
  }
  if (!category.includes('mattresses')) continue
  if (!isMattressProduct(name)) continue

  const price = parseInt(priceLine, 10)
  const firstImage = imagesLine
    .split(/\s*\|\s*/)
    .map((x) => x.trim())
    .find(Boolean)
  blocks.push({
    category: normalizeText(category),
    name: normalizeText(name),
    price,
    description: normalizeText(desc),
    image_url: firstImage,
    images_url: imagesLine.trim(),
    product_url: urlLine.trim(),
  })
}

const byUrl = new Map()
for (const b of blocks) {
  const prev = byUrl.get(b.product_url)
  if (!prev || b.description.length > prev.description.length) byUrl.set(b.product_url, b)
}
const unique = [...byUrl.values()].sort((a, b) => a.name.localeCompare(b.name))

let sql = `-- Mattress & bed mattress catalog (IKEA India export, mattress-related rows only).
-- Generated by scripts/generate-mattress-seed-sql.mjs — re-run after updating tmp_ikea_paste.txt

INSERT INTO mattress_products (category, name, price, description, image_url, images_url, product_url) VALUES\n`

const rows = unique.map(
  (b) =>
    `  (${sqlStr(b.category)}, ${sqlStr(b.name)}, ${b.price}, ${sqlStr(b.description)}, ${sqlStr(b.image_url)}, ${sqlStr(b.images_url)}, ${sqlStr(b.product_url)})`
)

sql += rows.join(',\n') + ';\n'

const out = path.join(root, 'supabase', 'migrations', '012_mattress_products_seed.sql')
fs.writeFileSync(out, sql, 'utf8')
console.log('products', unique.length, '->', out)
