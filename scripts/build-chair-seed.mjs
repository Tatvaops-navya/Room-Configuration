/**
 * Merges Urban Ladder + IKEA chair rows and writes supabase/migrations/018_chair_products_seed.sql
 * Regenerate IKEA slice: node scripts/parse-ikea-chairs.mjs
 * Run from repo root: node scripts/build-chair-seed.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { urbanLadderChairs } from './urban_ladder_chairs.mjs';
import { ikeaChairsParsed } from './ikea_chairs_parsed.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const outPath = path.join(root, 'supabase', 'migrations', '018_chair_products_seed.sql');

function dollarQuote(s) {
  const str = String(s ?? '');
  let tag = 'd';
  while (str.includes('$' + tag + '$')) tag += 'x';
  return '$' + tag + '$' + str + '$' + tag + '$';
}

function sqlVal(v) {
  if (v === null || v === undefined || v === '') return 'NULL';
  if (typeof v === 'number' && Number.isFinite(v)) return String(v);
  return dollarQuote(v);
}

const raw = [...urbanLadderChairs, ...ikeaChairsParsed];
const rows = raw.map((r) => ({
  category: r.c ?? null,
  name: r.n,
  brand: r.b ?? null,
  colour: r.co ?? null,
  warranty_in_months: r.w ?? null,
  country_of_origin: r.coo ?? null,
  length: r.le ?? null,
  width: r.wi ?? null,
  height: r.hi ?? null,
  net_weight: r.nw ?? null,
  description: r.d ?? null,
  generic_name: r.g ?? null,
  primary_material_type: r.m ?? null,
  primary_material_subtype: r.ms ?? null,
  primary_room: r.pr ?? null,
  seating_capacity: r.sc ?? null,
  product_model_name: r.pm ?? null,
  price: r.p ?? null,
  images_url: r.iu ?? null,
  product_url: r.pu,
}));

const urls = rows.map((r) => r.product_url).filter(Boolean);
const header = `-- Chair catalog seed (generated by scripts/build-chair-seed.mjs)
-- Sources: scripts/urban_ladder_chairs.mjs + scripts/ikea_chairs_parsed.mjs

DELETE FROM chair_products WHERE product_url = ANY(ARRAY[
${urls.map((u) => dollarQuote(u)).join(',\n')}
]);

INSERT INTO chair_products (
  category, name, brand, colour, warranty_in_months, country_of_origin,
  length, width, height, net_weight, description,
  generic_name, primary_material_type, primary_material_subtype, primary_room, seating_capacity, product_model_name,
  price, images_url, product_url
) VALUES
`;

const valueLines = rows.map(
  (r) =>
    `(${[
      sqlVal(r.category),
      sqlVal(r.name),
      sqlVal(r.brand),
      sqlVal(r.colour),
      r.warranty_in_months == null ? 'NULL' : String(parseInt(r.warranty_in_months, 10)),
      sqlVal(r.country_of_origin),
      sqlVal(r.length),
      sqlVal(r.width),
      sqlVal(r.height),
      sqlVal(r.net_weight),
      sqlVal(r.description),
      sqlVal(r.generic_name),
      sqlVal(r.primary_material_type),
      sqlVal(r.primary_material_subtype),
      sqlVal(r.primary_room),
      sqlVal(r.seating_capacity),
      sqlVal(r.product_model_name),
      r.price == null ? 'NULL' : String(Number(r.price)),
      sqlVal(r.images_url),
      sqlVal(r.product_url),
    ].join(', ')})`
);

const sql = header + valueLines.join(',\n') + ';\n';
fs.writeFileSync(outPath, sql, 'utf8');
console.log('Wrote', outPath, '(' + rows.length + ' rows)');
