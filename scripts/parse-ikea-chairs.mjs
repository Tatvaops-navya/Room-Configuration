/**
 * Parse scripts/tmp_ikea_paste.txt for IKEA chair-related SKUs:
 * - Office / swivel chairs under tables-fu004 (before first chairs-fu002)
 * - Everything under chairs-fu002 that mentions chair, stool, or dining set with chairs
 * Excludes standalone tables (no chair/stool in title).
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const tmpPath = path.join(__dirname, 'tmp_ikea_paste.txt');

const text = fs.readFileSync(tmpPath, 'utf8');
const lines = text.split(/\r?\n/);

function isCategoryLine(line) {
  return /^in\/en\/cat\//.test(line.trim());
}

function isPriceLine(line) {
  return /^\d+$/.test(line.trim());
}

function isImageLine(line) {
  return line.includes('ikea.com') && line.includes('|') && line.includes('.jpg');
}

function isProductUrl(line) {
  return /^https:\/\/www\.ikea\.com\/in\/en\/p\//.test(line.trim());
}

function shouldIncludeTitle(title) {
  const t = title.toLowerCase();
  if (/\bfoot-rest\b|\bfoot rest\b/i.test(t)) return false;
  const hasSeat = /\bchairs?\b|\bstools?\b/i.test(t);
  if (/\btable\b/.test(t) && !hasSeat) return false;
  return hasSeat;
}

function genericFromTitle(title) {
  const t = title.toLowerCase();
  if (/\boffice chair\b|\bswivel chair\b/.test(t)) return 'Office chair';
  if (/\btable and \d+ chairs?\b/.test(t) || /\btable and \d+ stools?\b/.test(t)) return 'Dining set';
  if (/\bchair\b/.test(t)) return 'Chair';
  if (/\bstool\b/.test(t)) return 'Stool set';
  return 'Chair';
}

function parseSection(startLineIdx, endLineIdx) {
  const out = [];
  let i = startLineIdx;
  while (i < endLineIdx) {
    const line = lines[i];
    if (!line || isCategoryLine(line)) {
      i += 1;
      continue;
    }
    const title = line.trim();
    if (!title || isPriceLine(title) || isImageLine(title) || isProductUrl(title)) {
      i += 1;
      continue;
    }
    if (!shouldIncludeTitle(title)) {
      i += 1;
      continue;
    }
    const pi = i + 1;
    if (pi >= endLineIdx || !isPriceLine(lines[pi])) {
      i += 1;
      continue;
    }
    const price = Number(lines[pi].trim());
    const di = pi + 1;
    if (di >= endLineIdx) break;
    let desc = lines[di].trim();
    let ii = di + 1;
    if (!isImageLine(lines[ii])) {
      i += 1;
      continue;
    }
    const images = lines[ii].trim();
    const ui = ii + 1;
    if (ui >= endLineIdx || !isProductUrl(lines[ui])) {
      i += 1;
      continue;
    }
    const productUrl = lines[ui].trim().split('?')[0];
    if (desc === title || !desc) desc = title;
    const wMatch = desc.match(/(\d+)\s*year\s*warranty/i);
    const warrantyMonths = wMatch ? Number(wMatch[1]) * 12 : null;
    out.push({
      c: 'IKEA / Chairs',
      n: title,
      b: 'IKEA',
      co: null,
      w: warrantyMonths,
      le: null,
      wi: null,
      hi: null,
      nw: null,
      d: desc,
      g: genericFromTitle(title),
      m: null,
      p: price,
      iu: images.replace(/\s*\|\s*/g, ' | '),
      pu: productUrl,
    });
    i = ui + 1;
  }
  return out;
}

const firstChairsFu002 = lines.findIndex((l) => l.trim() === 'in/en/cat/chairs-fu002');
const firstTablesFu004Office = lines.findIndex(
  (l, idx) => idx > 600 && l.includes('GRÖNFJÄLL office chair')
);

let office = [];
if (firstTablesFu004Office >= 0) {
  const endOffice = firstChairsFu002 >= 0 ? firstChairsFu002 : lines.length;
  office = parseSection(firstTablesFu004Office - 1, endOffice).map((r) => ({
    ...r,
    c: 'IKEA / Office chairs',
  }));
}

let dining = [];
if (firstChairsFu002 >= 0) {
  dining = parseSection(firstChairsFu002, lines.length).map((r) => ({
    ...r,
    c: 'IKEA / Dining & chairs',
  }));
}

const byUrl = new Map();
for (const r of [...office, ...dining]) {
  byUrl.set(r.pu, r);
}
const merged = [...byUrl.values()];

const outPath = path.join(__dirname, 'ikea_chairs_parsed.mjs');
const body = `/** Auto-generated by scripts/parse-ikea-chairs.mjs — do not edit by hand. */\nexport const ikeaChairsParsed = ${JSON.stringify(merged, null, 2)};\n`;
fs.writeFileSync(outPath, body, 'utf8');
console.log('Wrote', outPath, merged.length, 'rows');
